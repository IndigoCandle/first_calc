def multiply(first_num,second_num):
    if first_num.isdigit() and second_num.isdigit:
        return first_num * second_num
    else:
        print("Invalid - values must be numbers")
        return None
def subtract(first_num,second_num):
    if first_num.isdigit() and second_num.isdigit:
        return first_num - second_num
    else:
        print("Invalid - values must be numbers")
        return None
